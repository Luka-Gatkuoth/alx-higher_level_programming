#!/usr/bin/python3

def add(a, b):
    """
    my addition function

    args:
        a: first integer
        b: second integer
    Returns:
        The return value . a + b
    """
    return (a + b)


def sub(a, b):
    """
    my subtraction function

    args:
        a: first integer
        b: second integer
    
    Returns:
        The return value . a - b
    """
    return (a - b)


def mul(a, b):
    """
    my multiplication function

    args:
        a: first integer
        b: second integer

    Returns:The return value . a * b
    """
    return (a * b)


def div(a, b):
    """
    my division function
    
    args:
        a: first integer
        b: second integer

    Return:
        The return value . a / b
    """
    return (a / b)
